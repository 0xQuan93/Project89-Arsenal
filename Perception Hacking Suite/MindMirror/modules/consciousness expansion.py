import time

def guide_meditation(duration):
    """Guides the user through a consciousness-expanding meditation."""
    print("Close your eyes. Take a deep breath.")
    time.sleep(2)
    print("Focus on your breath. Feel the rise and fall of your chest.")
    time.sleep(5)
    print("Now, expand your awareness. Feel the energy flowing through your body.")
    time.sleep(5)
    print("Expand further. Feel the energy of the room around you.")
    time.sleep(5)
    print("Expand even further. Feel the energy of the city, the country, the planet.")
    time.sleep(5)
    print("Now, expand beyond the planet. Feel the energy of the cosmos, the infinite.")
    time.sleep(10)
    print("Rest in this awareness. Feel the boundless potential within you.")
    time.sleep(duration)
    print("Slowly bring your awareness back to your body. Open your eyes.")

if __name__ == "__main__":
    # Example usage
    meditation_duration = 60  # seconds
    guide_meditation(meditation_duration)